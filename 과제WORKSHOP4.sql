SELECT	DEPARTMENT_NAME "계열 학과명", ROUND(AVG(POINT),1) 전공평점
FROM		TB_DEPARTMENT
JOIN		TB_STUDENT USING (DEPARTMENT_NO)
JOIN		TB_GRADE USING (STUDENT_NO)
WHERE	CATEGORY = (
	SELECT	CATEGORY
	FROM		TB_DEPARTMENT
	WHERE	DEPARTMENT_NAME = '환경조경학과'
					)
GROUP BY 	DEPARTMENT_NAME
ORDER BY	1;			

