SELECT      CLASS_NO,
                CLASS_NAME,
                AVG(POINT)
FROM        TB_CLASS A
JOIN          TB_GRADE USING (CLASS_NO)
RIGHT JOIN  TB_DEPARTMENT D ON (A.DEPARTMENT_NO = D.DEPARTMENT_NO)
WHERE       D.DEPARTMENT_NAME = '환경조경학과'  AND A.CLASS_TYPE LIKE '전공%'
GROUP BY CLASS_NO, CLASS_NAME
ORDER BY 1;

