-- 수정요망 인출된 행의 개수 245
SELECT STUDENT_NO, STUDENT_NAME ,TO_DATE(SUBSTR(STUDENT_SSN,1,6)),ENTRANCE_DATE
FROM TB_STUDENT
WHERE MONTHS_BETWEEN(ENTRANCE_DATE, TO_DATE(SUBSTR(STUDENT_SSN,1,6)))/12 > 19
ORDER BY STUDENT_NO ASC;

