SELECT	PROFESSOR_NAME,
		PROFESSOR_SSN
FROM		TB_PROFESSOR
WHERE	LENGTH(PROFESSOR_NAME) <> 3;

--3
SELECT	PROFESSOR_NAME 교수이름,
		TO_CHAR(SYSDATE,'RRRR') - ('19'||SUBSTR(PROFESSOR_SSN,1,2)) 나이 
FROM		TB_PROFESSOR
WHERE	SUBSTR(PROFESSOR_SSN,8,1) = 1
ORDER BY	2;

