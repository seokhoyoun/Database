SELECT	STUDENT_NAME,
		STUDENT_SSN
FROM		TB_STUDENT
WHERE	ABSENCE_YN = 'Y'
ORDER BY 	2 DESC;

--3
SELECT	STUDENT_NAME,
		STUDENT_NO,
		STUDENT_ADDRESS
FROM		TB_STUDENT
WHERE	STUDENT_ADDRESS LIKE '강원도%' OR STUDENT_ADDRESS LIKE '경기도%'
AND		STUDENT_NO NOT LIKE 'A%'
ORDER BY 	1;

--4
SELECT	DEPARTMENT_NO
FROM		TB_DEPARTMENT
WHERE	DEPARTMENT_NAME = '법학과';

SELECT	PROFESSOR_NAME,
		PROFESSOR_SSN
FROM		TB_PROFESSOR
